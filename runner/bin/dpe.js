#!/usr/bin/env node
// dispatch shim for the `dpe` npm wrapper.
//
// On install, npm pulls in exactly one of the @combycode/dpe-<os>-<cpu>
// optional dependencies based on the host's os+cpu. This script resolves
// that package, writes a small config.toml next to the binary so the
// runner can find the bundled standard tools, and execs the binary with
// the user's argv passed through.
//
// Errors loudly if no platform package is installed (e.g. user passed
// --no-optional, or the host platform isn't supported).

"use strict";
const { spawnSync } = require("node:child_process");
const path = require("node:path");
const fs = require("node:fs");

const platform =
    process.platform === "win32"  ? "win32"  :
    process.platform === "darwin" ? "darwin" :
                                    "linux";
const arch  = process.arch === "arm64" ? "arm64" : "x64";
const pkg   = `@combycode/dpe-${platform}-${arch}`;
const exe   = process.platform === "win32" ? ".exe" : "";

let pkgDir;
try {
    pkgDir = path.dirname(require.resolve(`${pkg}/package.json`));
} catch {
    console.error(
        `dpe: native package "${pkg}" not installed for ${platform}-${arch}.\n` +
        `If you used --no-optional or installed under a non-matching host, run:\n` +
        `  npm install -g ${pkg}\n` +
        `or simply: npm install -g dpe (re-resolves optionalDependencies)`
    );
    process.exit(1);
}

const binPath = path.join(pkgDir, "bin", `dpe${exe}`);
if (!fs.existsSync(binPath)) {
    console.error(`dpe: binary missing at ${binPath} — package corruption; reinstall dpe.`);
    process.exit(1);
}

// Bootstrap a <bin>/config.toml that points the runner at the bundled
// standard tools + catalog. We write this ONLY when:
//   (a) the file doesn't exist yet (first run after install), OR
//   (b) the file still carries our marker first-line — meaning no human
//       has touched it, so refreshing it after a version bump is safe.
//
// If the user (or any other tool) edited the file, the marker is gone, and
// we never overwrite again. Manual edits survive every subsequent invocation.
//
// In v2.0.0 this block was an unconditional fs.writeFileSync on every run,
// which clobbered manual edits. v2.0.1+: cooperative, hands-off after edit.
const MARKER = "# Auto-generated by dpe npm wrapper. Edit ~/.dpe/config.toml or remove this line to opt out of regeneration.";
const cfgPath    = path.join(pkgDir, "bin", "config.toml");
const toolsDir   = path.join(pkgDir, "tools").replace(/\\/g, "/");
const catalog    = path.join(pkgDir, "catalog.json").replace(/\\/g, "/");
const cfgBody =
    `${MARKER}\n` +
    `tools_paths = ["${toolsDir}"]\n` +
    `tools_registries = ["${catalog}"]\n`;
try {
    let shouldWrite = true;
    if (fs.existsSync(cfgPath)) {
        // Read just the first line — cheap, avoids loading large user configs.
        const existing = fs.readFileSync(cfgPath, "utf8");
        const firstLine = existing.split("\n", 1)[0];
        // Tolerate trailing CR or whitespace from Windows editors.
        if (firstLine.trim() !== MARKER.trim()) {
            shouldWrite = false; // user-edited; respect that.
        }
    }
    if (shouldWrite) {
        fs.writeFileSync(cfgPath, cfgBody);
    }
} catch { /* read-only fs — runner will fall back */ }

const child = spawnSync(binPath, process.argv.slice(2), { stdio: "inherit" });
process.exit(child.status ?? 1);
